/*
1. TIPOS DE BANCOS DE DADOS
1.1 Bancos de dados relacionais
	* Principal modelo de representacao de dados digitais.
		* Surgiu na decada de 70, se tornou amplamente viavel a partir da decada de 80.
        * Seu principal padrao de comunicacao eh a linguagem SQL.
	* Conceitos (abstracoes) principais:
		* Tabelas, que sao as estruturas de dados
        * Relacoes, que identificam referencias entre dados de diferentes tabelas.

1.2 Bancos de dados nao-relacionais
	* Normalmente chamados de NoSQL: "not only SQL"
		* Armazena dados que nem sempre podem ser encaixados em tabelas.alter
			* Semi Estruturados
		* Popularizados a partir do seculo XXI com a web 2.0
	* Conceito (abstracao) principal:
		* Documentos, que codificam dados em algum formato padrao (ex.: XML, JSON)
	* Registram atributos por meio de chave e valor.

2. A LINGUAGEM SQL
2.1 Origem
	* Surgiu a partir do produto chamado SEQUEL, da IBM.
		* Criado para padronizar a forma de acessar e manipular dados.
	* Nome significa "Linguagem de Consulta Estruturada" (Structured Query Language)
	* Se manteve popular ao passar do tempo entre profissionais

2.2 Tipos de comandos
	* DDL (Data Definition Language)
    	* DQL (Data Query Language)
    	* DML (Data Manipulation Language)
    	* DCL (Data Control Language)
    	* TCL (Transaction Control Language)
*/
